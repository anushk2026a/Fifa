/** A curated place. Phone is intentionally omitted unless verified.
 *  `image` is an optional photo URL (e.g. from the place's Google Maps listing)
 *  rendered as a thumbnail that links to the map. */
function place(
  name: string,
  city: string,
  distanceMiles: number,
  address: string,
  note?: string,
  image?: string,
  mapUrl?: string,
): Place {
  return {
    name,
    address,
    mapUrl: mapUrl ?? mapsSearch(`${name}, ${city}`),
    distanceMiles,
    note,
    image,
  };
}

function transport(
  stadium: string,
  city: string,
  metro: { title: string; url: string; note?: string; image?: string },
  video?: string, // optional YouTube travel guide for reaching the stadium
): TransportOption[] {
  return [
    {
      category: "shared_ride",
      title: "Rideshare & taxi pickup",
      url: mapsSearch(`${stadium} rideshare pickup, ${city}`),
      note: "Walk to the stadium's marked rideshare area on match days, or request a ride straight there. Lyft and taxis use the same pickup zones.",
      image: "/images/transport/rideshare.webp",
      links: [
        { label: "Request an Uber", url: uberLink(`${stadium}, ${city}`) },
        {
          label: "Taxis nearby",
          url: mapsSearch(`taxi near ${stadium}, ${city}`),
        },
      ],
    },
    {
      category: "metro",
      image: "/images/transport/metro.webp",
      ...metro,
    },
    {
      category: "parking",
      title: "Pre-book stadium parking",
      url: "https://www.justpark.com/",
      note: "Reserve a spot ahead of match day to skip the queues.",
      image: "/images/transport/parking.webp",
    },
    {
      category: "getting_there",
      title: `Getting there | ${stadium} | FIFA World Cup 2026`,
      url: FIFA_TOURNAMENT_URL,
      video,
      image: "/images/transport/stadium.webp",
    },
  ];
}

function fanFest(city: string, note?: string, image?: string): ScreeningZone[] {
  return [
    {
      name: `${city} FIFA Fan Festival`,
      type: "fan_festival",
      url: FIFA_TOURNAMENT_URL,
      note:
        note ??
        "Free live screenings, food and music during the tournament (venue confirmed closer to kick-off).",
      image:
        image ?? "https://loremflickr.com/600/400/soccer,fans,crowd?lock=305",
    },
    {
      name: `${city} Downtown Watch Party`,
      type: "public_viewing",
      url: FIFA_TOURNAMENT_URL,
      note: "Free big-screen public viewing in the city centre on match days.",
      image: "https://loremflickr.com/600/400/bigscreen,crowd?lock=306",
    },
    {
      name: `Sports bars & pubs in ${city}`,
      type: "local_event",
      url: mapsSearch(`sports bars showing soccer, ${city}`),
      note: "Catch every match with fellow fans at nearby sports bars and pubs.",
      image: "https://loremflickr.com/600/400/sportsbar,pub?lock=307",
    },
  ];
}

function seo(name: string, stadium: string) {
  return {
    title: `${name} — FIFA World Cup 2026 Guide | FIFA One Point`,
    description: `Restaurants, hotels, transportation, tickets and fan zones near ${stadium} for FIFA World Cup 2026 in ${name}.`,
  };
}

